pub fn parse_country_code(code: &str) -> String {
    let country = match code {
        "US" => "United States",
        "CA" => "Canada",
        "GB" | "UK" => "United Kingdom",
        "DE" => "Germany",
        "FR" => "France",
        "ES" => "Spain",
        "IT" => "Italy",
        "JP" => "Japan",
        "CN" => "China",
        "IN" => "India",
        "BR" => "Brazil",
        "RU" => "Russia",
        "MX" => "Mexico",
        "AU" => "Australia",
        "NL" => "Netherlands",
        "SE" => "Sweden",
        "CH" => "Switzerland",
        "AR" => "Argentina",
        "BE" => "Belgium",
        "PL" => "Poland",
        "NO" => "Norway",
        "DK" => "Denmark",
        "FI" => "Finland",
        "IE" => "Ireland",
        "NZ" => "New Zealand",
        "KR" | "KO" => "South Korea",
        "ZA" => "South Africa",
        "NG" => "Nigeria",
        "EG" => "Egypt",
        "TR" => "Turkey",
        "SA" => "Saudi Arabia",
        "IR" => "Iran",
        "AE" => "United Arab Emirates",
        "HK" => "Hong Kong",
        "TW" => "Taiwan",
        "SG" => "Singapore",
        "MY" => "Malaysia",
        "TH" => "Thailand",
        "ID" => "Indonesia",
        "PH" => "Philippines",
        "VN" => "Vietnam",
        "PK" => "Pakistan",
        "BD" => "Bangladesh",
        "LK" => "Sri Lanka",
        "CZ" => "Czech Republic",
        "GR" => "Greece",
        "PT" => "Portugal",
        "HU" => "Hungary",
        "RO" => "Romania",
        "BG" => "Bulgaria",
        "AT" => "Austria",
        _ => code,
    };
    country.to_string()
}

